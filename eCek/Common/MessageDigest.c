/**
 * @brief 计算 16 位循环冗余校验和（CRC16-IBM）。
 * @details 本算法使用标准的多项式：0x8005 (x^16 + x^15 + x^2 + 1)
 *  进行计算。本函数可多次调用，而不必一次提供完整的连续数据块。
 * @param crc 初始值或者上一次计算的校验值。分多次计算时，第一次应该为0.
 * @param data 本次要计算的数据缓冲。
 * @param len 本次要计算的数据长度。
 */
unsigned short MessageDigest_Crc16(unsigned short crc,
        const unsigned char * data, unsigned int len)
{
    while (len--)
    {
        crc = (crc >> 8) ^ s_tabCrc16[(crc ^ (*data++)) & 0xff];
    }

    return crc;
}

